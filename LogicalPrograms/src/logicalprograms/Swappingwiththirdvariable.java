package logicalprograms;

public class Swappingwiththirdvariable {
	public static void main(String[] args) {
		int a = 10;
		int b = 20;
		int temp = a;//a empty temp=10
		a = b;//b empty a=20
		b = temp;// b=10
		System.out.println("a is" +" " +a);
		System.out.println("b is" +" " +b);
		
				
	}
}
